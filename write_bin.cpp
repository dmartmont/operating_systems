#include <iostream>
#include <fstream>
#include <cstdlib>

using namespace std;

int main(int argc, char* argv[]) {
	if(argc != 2) {
		cerr << "Usage control: <name>" << endl;
		return 1;
	}
	char * name = argv[1];
	char prog[] = {0x50, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 
				   0x60, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 
				   0x80, 0xFF, 0xFF, 0xFF, 
				   0x70, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
				   0x90, 0xFF, 0xFF, 0xFF, 
				   0xDF, 0xFF, 0xFF, 0xFF, 
				   0xC0, 0xFF, 0xFF, 0xFF,
				   0xE0, 0xFF};
	ofstream fileprog(name, ios_base::binary);
	fileprog.write(prog, sizeof(prog));
	fileprog.close();

	return 0;
}
